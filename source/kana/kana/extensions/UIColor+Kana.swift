import UIKit

extension UIColor {
    class func kanaKeyRedColor() -> UIColor {
        //81.6, 0.8, 10.
        return UIColor(red: 0.816, green: 0.08, blue: 0.106, alpha: 1)
    }
    
    class func kanaKeyGrayColor() -> UIColor {
        return UIColor(red: 0.333, green: 0.333, blue: 0.333, alpha: 1)
    }
}
